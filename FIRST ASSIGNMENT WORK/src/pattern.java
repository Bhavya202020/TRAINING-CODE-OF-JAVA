
public class pattern {

	public static void main(String[] args) {
		int i,j,k,n;
		n = 5;
		System.out.print(n + " ");
		for(i=1;i<=n;i++)
		    {
		        k = i;
		        for(j=1;j<=i;j++,k++)
		        {
		System.out.print(k + " ");
		}
		System.out.println();
        }
 
        }
	}



